    {
        $validated = $request->validate([
            'user_id' => ['required', 'integer'],
            'notif' => ['required', 'string', 'max:255'],
        ]);
        Notification::create($validated);
        Alert::success('Success', 'Add Notification Success');
        return redirect()->route('home');
    }

